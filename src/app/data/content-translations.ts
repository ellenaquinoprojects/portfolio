export function getTranslatedContent<T>(
  path: string,
  language: Language,
  fallback?: T
): T {
  const keys = path.split('.');
  let current: any = contentTranslations;
  
  for (const key of keys) {
    if (current && typeof current === 'object' && key in current) {
      current = current[key];
    } else {
      return fallback as T;
    }
  }
  
  if (current && typeof current === 'object' && language in current) {
    return current[language] as T;
  }
  
  return (fallback || current) as T;
}
